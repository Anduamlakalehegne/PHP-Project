    <h3>Major Types of Taxes in Ethiopia:</h3>
    <p class="left"> The major types of taxes that exist in Ethiopia, their meaning, rates and conditions, as  
provided by the Federal Inland Revenue Authority, are presented as follows:</p>
   
     <ol class="left">
        <li ><b>Value Added Tax (VAT)</b></li>
        <p class="left">This is a sales tax based on the increase in value or price of product at each stage in its  
manufacture and distribution. The cost of the tax is added to the final price and is  
eventually paid by the consumer.</p>
        <li><b>Excise Tax</b></li>
        <p class="left">This is imposed and payable on selected goods, such as, luxury goods and basic goods  
which are demand inelastic. In addition, it is believed that imposing the tax on goods that  
are hazardous to health and which are cause to social problems will reduce the  
consumption thereof. Excise tax shall be paid on goods mentioned under the sc hedule of  
'Excise Tax Proclamation No. 307/2002' (a) when imported and (b) when produced  
locally at the rate prescribed in the schedule. Computation of excise tax is applied (a) in  
the case of goods produced locally, production cost and (b) in the case of imported goods,  
cost, insurance and freight /C.I.F./. Payment of excise tax for locally produced goods is  
by the producer and for imported goods by the importer. Time of payment of excise tax  
for imported goods is at the time of clearing the goods from the customs area, and for  
locally produced goods it is not later than 30 days from the date of production.</p>
        <li><b>Turnover Tax</b></li>
        <p class="left">This is an equalisation tax imposed on persons not registered for value-added tax to fulfil  
their obligations and also to enhance fairness in commercial relations and to complete the  
coverage of the tax system. Administrative feasibility considerations limit the registration  
of persons under the value-added tax to those with annual transactions to the total value  
exceeding 500,000 Birr. 
Rate of turnover tax is 2% on goods sold locally and 10% on others; as provided by the  
'Excise Tax Proclamation No. 307/2002'.</p>
        <li><b>Income Tax</b></li>
        <p> Income taxable under the Ethiopian 'Income Tax Proclamation No. 286/2002' shall  
include, but not be limited to: <br>
<ul  class="left">
  <li>Income from employment;</li>
  <li> Income from business activities;</li>
  <li> Income derived by an entertainer, musician, or sports person from his personal activities;</li>
  <li>Income from entrepreneurial activities carried out by a non-resident through a permanent establishment in Ethiopia;</li>
  <li> Income from movable property attributable to a permanent establishment in  
Ethiopia;</li>
  <li>Income from immovable property and appurtenances thereto, income from  
livestock and inventory in agriculture and forestry, and income from usufruct and  
other rights deriving from immovable property that is situated in Ethiopia;</li>
  <li>Income from the alienation of property referred to in (e); </li>
  <li> Dividends distributed by a resident company;</li>
  <li> Profit shares paid by a resident registered partnership;</li>
  <li>Interest paid by the national, a regional or local Government or a resident of  
Ethiopia, or paid by a non-resident through a permanent establishment that he  
maintains in Ethiopia;</li>
</ul>
