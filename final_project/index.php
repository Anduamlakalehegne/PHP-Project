    <h2 >Ethiopian Tax</h2>
    <p class="left">Taxes are important sources of public revenue. Public goods and services are normally subject to collective consumption, thus requiring that we put some of what we earn into  
government hands. Public goods are normally supplied by public agencies due to their natures of non-rivalry and non-excludability. The nature of consumption of public goods  
is such that consumption by one does not reduce consumption for others. Besides, consumption of public goods by an agent does not exclude others from doing same. Such  
nature of public goods therefore makes them impossible for private suppliers to avail them at market prices like other commodities. Government intervention in the supply of  
public goods is, therefore, inevitable and can only be done if the public pays taxes for the production and supply of such goods.</p>
